# borra archivos de /tmp con el nombre rep12034tmp 
DIRECTORY=/tmp

function _delete_temp() {
  cd $DIRECTORY
  ls -C1 | grep -E 'rep[[:digit:]]+tmp' | while read line;do rm -rf $line; done
}

_delete_temp
